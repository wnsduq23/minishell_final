#include "libft.h"

int	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s && s[i])
		i++;
	return (i);
}

int	ft_strlencustom(char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
